# This program converts temperature in degrees Fahrenheit
# Into degrees Celsius

# Get the temperature in degrees Fahrenheit
fahr = float(input("Enter a temperature in degrees Fahrenheit  ?"))

# Convert into Celsius and print it
celsius = (fahr - 32) *5/9
print(fahr, "degrees Fahrenheit is ", celsius, " degrees Celsius")

